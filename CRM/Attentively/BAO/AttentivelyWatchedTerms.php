class CRM_Attentively_BAO_AttentivelyWatchedTerms extends CRM_Attentively_DAO_AttentivelyWatchedTerms {

  /**
   * Fetch object based on array of properties.
   *
   * @param array $params
   *   (reference ) an assoc array of name/value pairs.
   * @param array $defaults
   *   (reference ) an assoc array to hold the flattened values.
   *
   * @return CRM_Attentively_DAO_AttentivelyWatchedTerms
   */
  static public function getWatchedTerms(&$terms) {
    $dao = new CRM_Attentively_DAO_AttentivelyWatchedTerms();
    $dao->find();
    while ($dao->fetch()) {
      $terms[$dao->id]['term'] = $dao->term;
      $terms[$dao->id]['nickname'] = $dao->nickname;
      
      CRM_Core_DAO::storeValues($dao, $terms);

      return $dao;
    }
  } 

}